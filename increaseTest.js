/* 지시사항 1번 내용을 코드로 작성해보세요. */
var num = 20;

/* 지시사항 2번 내용을 코드로 작성해보세요. */
document.write(++num);

/* 지시사항 3번 내용을 코드로 작성해보세요. */
document.write(--num);

/* 지시사항 4번 내용을 코드로 작성해보세요. */
document.write(num++);
document.write(num);

/* 지시사항 5번 내용을 코드로 작성해보세요. */
document.write(num--);
document.write(num);
